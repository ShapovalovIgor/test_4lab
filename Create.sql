--Вложенная таблица
CREATE TYPE ACCESS_LEVEL IS TABLE OF NUMBER(1);
--Основная таблица
CREATE TABLE ACCOUNT (
       ID NUMBER(4)
       ,PERSONNEL_NUMBER NUMBER(4)
       ,FIRSTNAME VARCHAR2(50)
       ,LASTNAME VARCHAR2(60)
       ,MIDDLENAME VARCHAR2(60)
       ,LOGIN VARCHAR(20)
       ,PASSWORD CHAR(40)
       ,EMAIL VARCHAR(50)
       ,REG_DATE DATE
       ,PHONE_NUMBER NUMBER(10)
       ,ACCESS_L ACCESS_LEVEL) NESTED TABLE ACCESS_L STORE AS USER_ACCOUNT;
